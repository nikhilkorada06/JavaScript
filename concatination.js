let userName = "Nikhil";
let userAge = 19;

let msg = "My name is " + userName + " and my age is " + userAge + ".";
console.log(msg); // My name is Nikhil and my age is 19

let txt = `My name is ${userName} and my age is ${userAge}.`;

console.log(txt); // My name is Nikhil and my age is 19

//Both are clearly same 